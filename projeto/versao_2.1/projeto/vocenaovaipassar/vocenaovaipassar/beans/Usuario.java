package vocenaovaipassar.beans;

public class Usuario {

	
	
private int id;
private String usuario;
private String email;
private String data_cadastro;
private String data_ult_alter;
private String senha;
private String status;




public int getId() {	return id;}
public void setId(int id) {	this.id = id;}

public String getUsuario() {	return usuario;}
public void setUsuario(String usuario) {	this.usuario = usuario;}

public String getEmail() {	return email;}
public void setEmail(String email) {	this.email = email;}

public String getData_cadastro() {	return data_cadastro;}
public void setData_cadastro(String data_cadastro) {	this.data_cadastro = data_cadastro;}

public String getData_ult_alter() {	return data_ult_alter;}
public void setData_ult_alter(String data_ult_alter) {	this.data_ult_alter = data_ult_alter;}

public String getSenha() {	return senha;}
public void setSenha(String senha) {	this.senha = senha;}

public String getStatus() {	return status;}
public void setStatus(String status) {	this.status = status;}	
	
	
	
	

}
